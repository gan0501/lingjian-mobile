const AgreementScreen: React.FC<Props> = ({ navigation, route }) => {
  const insets = useSafeAreaInsets();
  const { type } = route.params || { type: 'user' };

  const agreement = agreementContents[type] || agreementContents.user;

  const renderContent = (content: string) => {
    const lines = content.split('\n');
    return lines.map((line, index) => {
      if (line.startsWith('# ')) {
        return (
          <Text key={index} style={styles.h1}>
            {line.replace('# ', '')}
          </Text>
        );
      }
      if (line.startsWith('## ')) {
        return (
          <Text key={index} style={styles.h2}>
            {line.replace('## ', '')}
          </Text>
        );
      }
      if (line.startsWith('### ')) {
        return (
          <Text key={index} style={styles.h3}>
            {line.replace('### ', '')}
          </Text>
        );
      }
      if (line.startsWith('#### ')) {
        return (
          <Text key={index} style={styles.h4}>
            {line.replace('#### ', '')}
          </Text>
        );
      }

      if (line.includes('**')) {
        const parts = line.split('**');
        return (
          <Text key={index} style={styles.paragraph}>
            {parts.map((part, i) =>
              i % 2 === 1 ? (
                <Text key={i} style={styles.bold}>{part}</Text>
              ) : (
                part
              )
            )}
          </Text>
        );
      }

      if (line === '---') {
        return <View key={index} style={styles.divider} />;
      }

      if (line.startsWith('|')) {
        return null;
      }

      if (line.trim()) {
        return (
          <Text key={index} style={styles.paragraph}>
            {line}
          </Text>
        );
      }

      return <View key={index} style={styles.emptyLine} />;
    });
  };

  return (
    <View style={[styles.container, { paddingTop: insets.top }]}>
      <View style={styles.header}>
        <BackButton
          onPress={() => navigation.goBack()}
          color="#333"
          backgroundColor="transparent"
        />
        <Text style={styles.headerTitle}>{agreement.title}</Text>
        <View style={styles.headerPlaceholder} />
      </View>

      <ScrollView
        style={styles.content}
        showsVerticalScrollIndicator={false}
      >
        {renderContent(agreement.content)}
        <View style={{ height: insets.bottom + 20 }} />
      </ScrollView>
    </View>
  );
};

